    private void ttfTaikhoanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ttfTaikhoanKeyReleased
        // TODO add your handling code here:
              try {
            // TODO add your handling code here:
            String query = ttfTaikhoan.getText().toLowerCase();
            TaiKhoanDB db = new TaiKhoanDB();
            if(db.checktk(query)){
                jbTBtaikhoan.setText("Tài Khoản Hợp Lệ");
                jbTBtaikhoan.setForeground(Color.GREEN);
        ImageIcon img = new ImageIcon(getClass().getResource("/qlch/Image/GiaoDien/ok_20px.png"));
        jbTBtaikhoan.setIcon(img);
                 
            }else{
                jbTBtaikhoan.setText("Tài Khoản Đã Tồn Tại");
                jbTBtaikhoan.setForeground(Color.red);
                ImageIcon img = new ImageIcon(getClass().getResource("/qlch/Image/GiaoDien/cancel_20px.png"));
                jbTBtaikhoan.setIcon(img);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DSSach.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_ttfTaikhoanKeyReleased

