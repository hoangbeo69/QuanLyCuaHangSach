    private  boolean  checkRong(){
         String diachi = ttfTaikhoan.getText();
         String manv = ttfManhanvien.getText();
         String tennv = ttfTennhanvien.getText();
         String gt = jbNam.isSelected()?"Nam":"Nữ";
         String ngaysinh = ttfNam.getText()+"/"+ttfThang.getText()+"/"+ttfNgay.getText();
         String sdt = ttfSdt.getText();
         String taikhoan = ttfTaikhoan.getText();
         String matkhau = ttfMatkhau.getText();
         SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");
         if(diachi.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Địa Chỉ");
         }else if(manv.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Mã Nhân Viên");
         }else if(tennv.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Tên Nhân Viên");
         }else if(gt.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Chọn Giới Tính");
         }else if(ngaysinh.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Ngày Sinh");
         }else if(sdt.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Số Điện Thoại");         
         }else if(taikhoan.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập tên tài khoản cần đăng ký");         
         }else if(matkhau.equals("")){
             JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Mật Khẩu để hoàn thành đăng ký mới");         
         }else{
             try {
             df.parse(ngaysinh);
         }   catch (ParseException ex) {
                 JOptionPane.showMessageDialog(null,"Bạn Cần Nhập Đúng Định Dạng Ngày Sinh "
                         + "27/09/1999");
         }
             return true;
         }
         return false;
    }

